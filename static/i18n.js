const LANGUAGE_STORAGE_KEY = "pinmap:language";
const DEFAULT_LANGUAGE = "pt-BR";

function getSavedLanguage() {
  const savedLanguage = localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return translations[savedLanguage] ? savedLanguage : DEFAULT_LANGUAGE;
}

function t(key) {
  const language = getSavedLanguage();
  return translations[language][key] || translations[DEFAULT_LANGUAGE][key] || key;
}

function translatePage() {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = t(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    element.placeholder = t(element.dataset.i18nPlaceholder);
  });
}

function setLanguage(language) {
  const nextLanguage = translations[language] ? language : DEFAULT_LANGUAGE;
  localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
  document.documentElement.lang = nextLanguage;
  translatePage();
  window.dispatchEvent(new CustomEvent("pinmap:languagechange"));
}

window.pinmapI18n = {
  getSavedLanguage,
  setLanguage,
  t,
  translatePage,
  translations,
};
